// Helper function to calculate average rating from reviews
const calculateAverageRating = (reviews) => {
  if (!reviews || reviews.length === 0) return 0;
  const sum = reviews.reduce((acc, review) => acc + review.rating, 0);
  return Math.round((sum / reviews.length) * 100) / 100;
};

// Enhance sites with calculated ratings
export const fantasySportsWithCalculatedRatings = fantasySports.map((site) => ({
  ...site,
  rating: calculateAverageRating(site.reviews),
}));

export const getSiteById = (id) => {
  const site = fantasySportsWithCalculatedRatings.find((site) => site.id === id);
  return site;
};
